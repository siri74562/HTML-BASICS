//syntax in javascript
//let,const, var are used to declare variables in JavaScript.
let a = 10;
//let is block scoped,cannot be redeclared in the same scope.
const b=20;
//const is block scoped and cannot be reassigned.
var c = 30;
//var is function scoped, can be redeclared in the same scope
console.log(a,b,c);
//console.log will output the values of a,b,c
// Output: 10 20 30
